#include <stdio.h>


int main() {

    100; // 10进制
    0b100; // 2进制，0b是2进制的前缀
    0100; // 8进制，0是8进制的前缀
    0x100; // 16进制，0x是16进制的前缀

    // %d 表示有符号的整数（4字节）
    // %p 是变量在内存中的地址
    // %s 表示字符串
    printf("%d\n", 100);
    printf("%d\n", 0b100);
    printf("%d\n", 0100);
    printf("%d\n", 0x100);
}