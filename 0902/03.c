/**
 *  标识符：
 *      - 规则：
 *          - 1. 字母、数字以及下划线
 *          - 2. 严格区分大小写
 *          - 3. 数字不可以作为首字符
 *          - 4. 不能系统关键字
 *              - 什么是关键字？是C语言中预定义使用的一些特殊字符
 * 
 *      - C语言中哪些内容必须符合标识符的规范
 *          - 变量
 *          - 函数名
 *          - 结构体名
 * 
 *      - 标识符命名时，遵循“见名知意”的惯例即可
 *      - if/else/int/float 都不可以 
*/