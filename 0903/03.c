/**
 *  C 语言中的基本数据类型：
 *      - C中对于数据类型所占字节，不是固定值，与运行环境有关
 *      - 以当前通用计算机来说：
 *      整数类型：
 *          char 1字节（固定）
 *          short [int]  2字节
 *          int 4字节 2^32
 *          long [int] 4字节
 *          long long [int] 8字节（固定）
 * 
 *          short <= int <= long
 *          “字”，总线宽度， int基本上“字”是相同的
 * 
 * 
 *      unsigned 无符号
 *      signed 有符号
 * 
 *      浮点数类型：
 *          float：4字节（32位，1-8-23）
 *          double：8字节（64位，1-11-52）
 * 
 * 
*/